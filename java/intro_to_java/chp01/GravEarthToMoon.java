class GravEarthToMoon {
  public static void main(String args[]) {
    double myWeightOnEarth;
    double myWeightOnMoon;

    myWeightOnEarth = 160;
    myWeightOnMoon = 160 * 0.17;

    System.out.println("On Earth I weigh " + myWeightOnEarth + "lbs.");
    System.out.println("On the moon, I weigh " + myWeightOnMoon + "lbs.");
  }
}
